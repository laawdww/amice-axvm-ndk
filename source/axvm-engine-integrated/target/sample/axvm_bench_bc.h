/*
 * 密集循环基准字节码（10000 次 ADD + 分支）：
 *   x0=i, x1=10000, x2=1, x3=acc
 * loop: ADD x3,x3,x2; ADD x0,x0,x2; CMP x0,x1; B.NE loop; MOV x0,x3; RET
 * 期望返回值 10000
 */
static const uint8_t g_axvm_bench_loop_bc[] = {
    0x41, 0x58, 0x56, 0x31,
    0x00, 0x00, 0x01, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x28, 0x00, 0x00, 0x00,
    0x3D, 0x00, 0x00, 0x00,
    0x65, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    0x00, 0x00, 0x00, 0x00,
    /* LDRI64 x0, 0 */
    0x10, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    /* LDRI64 x1, 10000 */
    0x10, 0x01, 0x10, 0x27, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    /* LDRI64 x2, 1 */
    0x10, 0x02, 0x01, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    /* LDRI64 x3, 0 */
    0x10, 0x03, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00, 0x00,
    /* loop @40: ADD x3,x3,x2 */
    0x13, 0x03, 0x03, 0x02,
    /* ADD x0,x0,x2 */
    0x13, 0x00, 0x00, 0x02,
    /* CMP x0,x1 */
    0x1A, 0x00, 0x01,
    /* B.NE loop (rel=-17) */
    0x41, 0x01, 0xEF, 0xFF, 0xFF, 0xFF,
    /* MOV x0,x3 */
    0x30, 0x00, 0x03,
    0x50,
};

#define AXVM_BENCH_LOOP_BC_SIZE   (sizeof(g_axvm_bench_loop_bc))
#define AXVM_BENCH_LOOP_EXPECT    10000u

#endif
